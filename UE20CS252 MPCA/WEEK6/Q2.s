.DATA
	A:.WORD -1,-2,1,2,-3,4,-1,-5,7,2
	SUM:.WORD 0

.TEXT
	LDR R0,=A
	LDR R1,=SUM
	MOV R2,#0 /*SUM=INITIALISED TO ZERO*/
	MOV R3,#0 /*COUNT*/
	BL LOOP
	STR R3,[R1]
	SWI 0X011

LOOP:	LDR R4,[R0],#4
	MOVS R4,R4
	BPL POSITIVE
L1:	
	ADD R3,R3,#1
	CMP R3,#10
	BNE LOOP
	MOV PC,LR

POSITIVE:
	ADD R2,R4,R2
	B L1