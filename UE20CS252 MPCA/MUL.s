.DATA
	A:.WORD 1,2,3
	B:.WORD 4,5,6
	C:.WORD 0
.TEXT
	LDR R1,=A
	LDR R2,=B
	LDR R3,=C
	MOV R4,#3
	MOV R5,#0
LOOP:   LDR R6,[R1],#4
	LDR R7,[R2],#4
	MUL R8,R6,R7
	ADD R5,R5,R8
	SUB R4,R4,#1
	TEQ R4,#0 
	BNE LOOP
